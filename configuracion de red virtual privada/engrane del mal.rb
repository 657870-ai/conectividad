require 'abrasiva.rb'

class Openvpn
	def generador_de_clave_secreta(--genkey)
		@secret = string.to_key
	servicio_de_encriptacion(:ProtectedCloudKeySyncing) do
		generador_de_clave_secreta = 'por favor ingrese un valor de texto para convertirlo en clave secreta, utilice una palabra facil de recordar, el sistema convierte esta palabra a una clave secreta encriptada y protegida con servicio de sincronización con sus dispositivos. Recuerde: No ingrese ningun tipo de texto encriptado que no pueda recordar fácilmente'
		generador_de_clave_secreta = gets
	end
	class Envio_de_clave_secreta(generador_de_clave_secreta)
		@@conexiones = {:ifconfig ip => neagent
						:"@#{dev tun}" => --secret string.to_key
						:remote => neagent}
						
	--dev tun = genkey if generador_de_clave_secreta('@secret')
	"abrasiva.rb#{Openvpn}"
end
